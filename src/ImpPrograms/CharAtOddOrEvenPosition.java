package ImpPrograms;

public class CharAtOddOrEvenPosition {

	public static void main(String[] args) {
		String s="JavaProgramming";
		for(int i=0;i<s.length();i++) {
			if(i%2!=0)
			{
				System.out.println("At Odd Position:"+s.charAt(i)+" ");
			}
			else
			{
				System.out.println("At Even Position:"+s.charAt(i)+" ");
			}
		}
		
		
		
		
		
		
		
		
		
		
		
	}
	
	
	
}
